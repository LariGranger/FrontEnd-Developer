//* tipos de dados primitivos no JS

//*String
"Olá";
`Olá`;
("213");
console.log('"Essa é uma citação!"');
console.log("Olá, " + "Mundo!");

//*Number
2;
2.5;
0.234984;
console.log(2 + 2.5);
console.log(0 / 0); //retorna NaN (not a number)
console.log(1 / 0); //o resultado tende pra infinito

//*Booleanos
true;
false;
console.log(!true);

//*Null: define a variável como vazia
let variavelNula = null;
console.log(variavelNula);

//*Undefined: variável que não teve nenhum valor atribuido à ela
let variavel;
console.log(variavel);
